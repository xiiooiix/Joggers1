package kkt.com.joggers.model;

public class Friend {
    private String id;
    private String imageUrl;

    public Friend(){}

    public Friend(String id){
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }
}
